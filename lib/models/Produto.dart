class Produto{
  String nome;
  int quantidadeIdeal;
  int quantidadeTotal;
  String fornecedor;

  Produto(nome, quantidadeIdeal, quantidadeTotal, fornecedor){
    this.nome = nome;
    this.quantidadeIdeal = quantidadeIdeal;
    this.quantidadeTotal = quantidadeTotal;
    this.fornecedor = fornecedor;
  }
}